package basicsofjava;

public class IfElseIfDemo {

	public static void main(String[] args)
	
	{
		int a=15, b=55, c=30;
		
		if(a>b && a>c)
			
		{
			System.out.println("a is big number");
		}
		
		else if(b>c)
			
		{
			System.out.println("b is big number");
		}
		
		else
		{
			System.out.println("c is big number");
		}

	}

}
